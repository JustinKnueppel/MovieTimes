/**
 * Format a date object into a yyyy-mm-dd date string.
 * @param date Date to be formatted.
 */
function formatDate(date) {
    let dd = date.getDate();
    dd = dd >= 10 ? dd : `0${dd}`;
    let mm = date.getMonth() + 1;
    mm = mm >= 10 ? mm : `0${mm}`;
    let yyyy = date.getFullYear();
    return `${yyyy}-${mm}-${dd}`;
}
