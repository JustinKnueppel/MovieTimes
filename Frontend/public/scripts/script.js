const ALL_DATA = getData();
/*
 * Filter all data by given constraints.
 */
function filterData(theatreIDs, startTime, endTime) {
    let filterData = {};
    for (let theatre of Object.keys(ALL_DATA)) {
        // Filter for theatre
        if (theatreIDs.indexOf(theatre) >= 0) {
            filterData[theatre] = JSON.parse(JSON.stringify(ALL_DATA[theatre]));
            // Filter for time
            for (let movie of filterData[theatre]) {
                movie.times = movie.times.filter(timeObj => {
                    let time = timeToDate(timeObj.time);
                    return time >= startTime && time <= endTime;
                });
            }
        }
    }
    return filterData;
}
/**
 * Load in showtimes in chronological order.
 */
function loadData(theatresData) {
    // Remove old data
    let table = document.getElementById('movie-table');
    for (let tr of document.querySelectorAll('#movie-table > tr:not([table-headers])')) {
        table.removeChild(tr);
    }
    // Load new data
    let showtimes = [];
    for (let theatre of AMCtheatres) {
        if (theatresData[theatre.id]) {
            showtimes.push(...getShowtimes(theatre, theatresData[theatre.id]));
        }
    }
    loadShowtimes(showtimes.sort((s1, s2) => {
        return s1.sortTime - s2.sortTime;
    }));
}
