/**
 * Return the current hour in 12 hour format along with an am/pm indicator.
 */
function getCurrentHour() {
    let date = new Date();
    let hour = date.getHours();
    let ampm = hour <= 11 ? 'am' : 'pm';
    if (hour > 12) {
        hour -= 12;
    }
    return [hour, ampm];
}
